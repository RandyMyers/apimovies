// Get all episodes airing in a given month (1st through last day). Public.
exports.getEpisodesForMonth = async (req, res) => {
  try {
    const year = req.query.year;
    const month = req.query.month;
    if (!year || !month) {
      return res.status(400).json({ error: 'year and month query params are required' });
    }
    const episodes = await episodeService.getEpisodesForMonth(year, month);
    res.json({ episodes });
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
};

// Get next episode for a specific show
exports.getNextEpisodeForShow = async (req, res) => {
  try {
    const { tvId } = req.params;
    const nextEpisode = await episodeService.getNextEpisodeForShow(tvId);

    if (!nextEpisode) {
      return res.json({ nextEpisode: null, message: 'No upcoming episodes' });
    }

    res.json({
      nextEpisode,
      countdown: episodeService.calculateCountdown(nextEpisode.air_date),
    });
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
};

// Enable auto-reminders for new episodes
exports.enableAutoReminders = async (req, res) => {
  try {
    const { tvId, reminderMinutes, notificationMethod } = req.body;

    if (!tvId) {
      return res.status(400).json({ error: 'tvId is required' });
    }

    const settings = await AutoReminderSettings.findOneAndUpdate(
      { userId: req.user._id, tmdbTvId: tvId },
      {
        userId: req.user._id,
        tmdbTvId: tvId,
        isEnabled: true,
        reminderMinutes: reminderMinutes || 60,
        notificationMethod: notificationMethod || 'in_app',
      },
      { upsert: true, new: true }
    );

    res.json({ settings });
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
};

// Disable auto-reminders for a show
exports.disableAutoReminders = async (req, res) => {
  try {
    const { tvId } = req.params;

    const settings = await AutoReminderSettings.findOneAndUpdate(
      { userId: req.user._id, tmdbTvId: parseInt(tvId) },
      { isEnabled: false },
      { new: true }
    );

    if (!settings) {
      return res.status(404).json({ error: 'Auto-reminder settings not found' });
    }

    res.json({ settings });
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
};


