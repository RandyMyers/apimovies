const CalendarEvent = require('../models/CalendarEvent');
const Reminder = require('../models/Reminder');
const AutoReminderSettings = require('../models/AutoReminderSettings');
const episodeService = require('../services/episodeService');

// ----- Calendar Events -----

exports.getEvents = async (req, res) => {
  try {
    const { from, to } = req.query;
    const query = { userId: req.user._id };

    if (from || to) {
      query.startDate = {};
      if (from) query.startDate.$gte = new Date(from);
      if (to) query.startDate.$lte = new Date(to);
    }

    const events = await CalendarEvent.find(query).sort({ startDate: 1 });
    res.json(events);
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
};

exports.getEvent = async (req, res) => {
  try {
    const event = await CalendarEvent.findOne({
      _id: req.params.id,
      userId: req.user._id,
    });

    if (!event) {
      return res.status(404).json({ error: 'Event not found' });
    }

    res.json(event);
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
};

exports.createEvent = async (req, res) => {
  try {
    const {
      title,
      description,
      eventType,
      tmdbId,
      mediaType,
      seasonNumber,
      episodeNumber,
      episodeId,
      startDate,
      endDate,
      isRecurring,
      recurrencePattern,
      location,
      attendees,
      reminderMinutes,
      isAutoGenerated,
    } = req.body;

    if (!title || !eventType || !startDate) {
      return res
        .status(400)
        .json({ error: 'title, eventType and startDate are required' });
    }

    const event = await CalendarEvent.create({
      userId: req.user._id,
      title,
      description,
      eventType,
      tmdbId,
      mediaType,
      seasonNumber,
      episodeNumber,
      episodeId,
      startDate,
      endDate,
      isRecurring: !!isRecurring,
      recurrencePattern: recurrencePattern || null,
      location,
      attendees: attendees || [],
      reminderMinutes: typeof reminderMinutes === 'number' ? reminderMinutes : null,
      isAutoGenerated: isAutoGenerated || false,
    });

    res.status(201).json(event);
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
};

exports.updateEvent = async (req, res) => {
  try {
    const update = { ...req.body, updatedAt: new Date() };

    const event = await CalendarEvent.findOneAndUpdate(
      { _id: req.params.id, userId: req.user._id },
      update,
      { new: true, runValidators: true },
    );

    if (!event) {
      return res.status(404).json({ error: 'Event not found' });
    }

    res.json(event);
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
};

exports.deleteEvent = async (req, res) => {
  try {
    const event = await CalendarEvent.findOneAndDelete({
      _id: req.params.id,
      userId: req.user._id,
    });

    if (!event) {
      return res.status(404).json({ error: 'Event not found' });
    }

    // Optionally delete associated reminders
    await Reminder.deleteMany({ calendarEventId: event._id });

    res.json({ message: 'Event deleted successfully' });
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
};

// ----- Reminders -----

exports.getReminders = async (req, res) => {
  try {
    const { from, to, isSent } = req.query;
    const query = { userId: req.user._id };

    if (from || to) {
      query.reminderDate = {};
      if (from) query.reminderDate.$gte = new Date(from);
      if (to) query.reminderDate.$lte = new Date(to);
    }

    if (typeof isSent !== 'undefined') {
      query.isSent = isSent === 'true';
    }

    const reminders = await Reminder.find(query).sort({ reminderDate: 1 });
    res.json(reminders);
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
};

